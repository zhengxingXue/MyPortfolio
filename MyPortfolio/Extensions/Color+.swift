import SwiftUI

extension Color {
    static let theme = ColorTheme()
    
    var uiColor: UIColor? {
        return UIColor(self)
    }
}

struct ColorTheme {
    let accent = Color("AccentColor")
    let background = Color("BackgroundColor")
    let green = Color("GreenColor")
    let red = Color("RedColor")
    let secondaryText = Color("SecondaryTextColor")
}
